package com.example.doan1.models;

public class Chuong {
    private int id;
    private String title;

    public Chuong(int id, String title) {
        this.id = id;
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }
}
